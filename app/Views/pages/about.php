	<h2>О пројекту</h2>
	<p>Пројекат <em>Рокови</em> је креиран са циљем да студентима Математичког факултета у Београду пружи увид у досадашње испитне рокове за неке од предмета.</p>
	<p>Институција Математичког факултета у Београду <b>није ни на који начин</b> повезана са овим пројектом.</p>
	<h3>Решења задатака</h3>
	<p>На овом се неће постављати и решења рокова. Решења потражите на страницама наставног особља, код Мише у копирници, у белешкама старијих колега или на интернету (нпр. на домаћем форуму <a href="matemanija.com"><em>Матеманија</em></a> или енглеском сајту <a href="https://math.stackexchange.com/"><em>Mathematics Stack Exchange</em></a>).</p>
