	<h2>О пројекту</h2>
	<p>Пројекат <i>МАТФ Рокови</i> је креиран са циљем да студентима Математичког факултета у Београду пружи увид у досадашње испитне рокове за неке од предмета.</p>
	<p>Институција Математичког факултета у Београду <b>није ни на који начин</b> повезана са овим пројектом.</p>
	<h3>Решења задатака</h3>
	<p>На овом сајту се неће постављати и решења рокова. Решења потражите на страницама наставног особља, код Мише у копирници, у белешкама старијих колега или на интернету (нпр. на домаћем форуму <a href="http://www.matemanija.com"><i>Матеманија</i></a> или на сајту <a href="https://math.stackexchange.com/"><i>Mathematics Stack Exchange</i></a>).</p>
