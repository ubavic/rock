		<li><b>Ако познајете <em>LaTex</em></b> можете нам помоћи са прекуцавањем рокова у дигиталан формат. За више детаља обратите се на мејл.</li>
		<li><b>Ако знате <em>PHP</em></b> можете се прикључити развоју веб сајта преко <a href="https://github.com/ubavic/rock"><em>GitHub</em>-а</a></li>
